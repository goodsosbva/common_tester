function renderAuthStatePath(targetId) {
  return `docs/common-tester/runtime/targets/${targetId}/.auth/storage-state.json`;
}

function renderAuthHelper(options = {}) {
  const targetId = options.targetId || 'unknown-target';
  const route = options.route || '/';
  const storageStatePath = renderAuthStatePath(targetId);
  const missingCredentialsMessage = options.missingCredentialsMessage || 'Missing E2E_USERNAME/E2E_PASSWORD for authenticated test.';

  return [
    `const TARGET_ROUTE = ${JSON.stringify(route)};`,
    `const AUTH_STATE_FILE = path.resolve(process.cwd(), ${JSON.stringify(storageStatePath)});`,
    `const AUTH_SESSION_FILE = path.resolve(process.cwd(), ${JSON.stringify(storageStatePath.replace(/storage-state\.json$/, 'session-storage.json'))});`,
    'const ACTION_TIMEOUT = Number(process.env.COMMON_TESTER_ACTION_TIMEOUT || 10000);',
    '',
    'async function attachFailureScreenshot(page, testInfo, name = "failure screenshot") {',
    '  if (!testInfo || !page || page.url() === "about:blank") return;',
    '  const screenshotPath = testInfo.outputPath(`${name.replace(/\\s+/g, "-")}.png`);',
    '  try {',
    '    await page.screenshot({ path: screenshotPath, fullPage: true });',
    '    if (fs.existsSync(screenshotPath)) {',
    '      await testInfo.attach(name, { path: screenshotPath, contentType: "image/png" });',
    '    }',
    '  } catch (_) {}',
    '}',
    '',
    'function ensureStorageStateFile() {',
    '  fs.mkdirSync(path.dirname(AUTH_STATE_FILE), { recursive: true });',
    '  let shouldWriteEmptyState = !fs.existsSync(AUTH_STATE_FILE);',
    '  if (!shouldWriteEmptyState) {',
    '    try {',
    "      const parsed = JSON.parse(fs.readFileSync(AUTH_STATE_FILE, 'utf8'));",
    '      shouldWriteEmptyState = !parsed || !Array.isArray(parsed.cookies) || !Array.isArray(parsed.origins);',
    '    } catch (_) {',
    '      shouldWriteEmptyState = true;',
    '    }',
    '  }',
    '  if (shouldWriteEmptyState) {',
    "    fs.writeFileSync(AUTH_STATE_FILE, JSON.stringify({ cookies: [], origins: [] }, null, 2));",
    '  }',
    '}',
    '',
    'ensureStorageStateFile();',
    '',
    'function readSavedSessionStorage() {',
    '  if (!fs.existsSync(AUTH_SESSION_FILE)) return null;',
    '  try {',
    "    const parsed = JSON.parse(fs.readFileSync(AUTH_SESSION_FILE, 'utf8'));",
    '    if (!parsed || !parsed.origin || !parsed.values || typeof parsed.values !== "object") return null;',
    '    return parsed;',
    '  } catch (_) {',
    '    return null;',
    '  }',
    '}',
    '',
    'async function installSavedSessionStorage(context) {',
    '  const saved = readSavedSessionStorage();',
    '  if (!saved) return;',
    '  await context.addInitScript((snapshot) => {',
    '    if (!snapshot || !snapshot.origin || location.origin !== snapshot.origin) return;',
    '    for (const [key, value] of Object.entries(snapshot.values || {})) {',
    '      window.sessionStorage.setItem(key, value == null ? "" : String(value));',
    '    }',
    '  }, saved);',
    '}',
    '',
    'async function saveSessionStorage(page) {',
    '  const snapshot = await page.evaluate(() => {',
    '    const values = {};',
    '    for (let index = 0; index < window.sessionStorage.length; index += 1) {',
    '      const key = window.sessionStorage.key(index);',
    '      if (key) values[key] = window.sessionStorage.getItem(key);',
    '    }',
    '    return { origin: location.origin, values };',
    '  }).catch(() => null);',
    '  if (!snapshot || !snapshot.origin) return;',
    '  await fs.promises.mkdir(path.dirname(AUTH_SESSION_FILE), { recursive: true });',
    '  await fs.promises.writeFile(AUTH_SESSION_FILE, JSON.stringify(snapshot, null, 2));',
    '}',
    '',
    'async function firstVisible(page, selectors, timeout = 120000) {',
    '  const startedAt = Date.now();',
    '  while (Date.now() - startedAt < timeout) {',
    '    for (const selector of selectors) {',
    '      const locator = typeof selector === "string" ? page.locator(selector).first() : selector.first();',
    '      if (await locator.waitFor({ state: "visible", timeout: 500 }).then(() => true).catch(() => false)) return locator;',
    '    }',
    '    await page.waitForTimeout(300);',
    '  }',
    '  return null;',
    '}',
    '',
    'async function visibleFill(locator, value) {',
    '  const text = value == null ? "" : String(value);',
    '  await expect(locator, "input must be visible before fill").toBeVisible({ timeout: ACTION_TIMEOUT });',
    '  await expect(locator, "input must be enabled before fill").toBeEnabled({ timeout: ACTION_TIMEOUT });',
    '  await locator.click({ timeout: ACTION_TIMEOUT });',
    '  await locator.fill("", { timeout: ACTION_TIMEOUT });',
    '  await locator.fill(text, { timeout: ACTION_TIMEOUT });',
    '}',
    '',
    'async function isLoginVisible(page, timeout = 3000) {',
    "  if (/\\/login/.test(page.url())) return true;",
    '  const loginSignal = await firstVisible(page, [',
    "    page.getByRole('button', { name: /로그인|Login/i }),",
    "    page.getByText(/운영자 로그인|아이디를 입력해주세요|비밀번호를 입력해주세요|Login/i),",
    "    'input[placeholder*=\"아이디\"]',",
    "    'input[placeholder*=\"비밀번호\"]',",
    '  ], timeout);',
    '  return Boolean(loginSignal);',
    '}',
    '',
    'async function hasAppContent(page) {',
    '  return page.evaluate(() => {',
    '    const root = document.querySelector("#root");',
    '    const bodyText = document.body ? document.body.innerText.trim() : "";',
    '    return Boolean((root && root.children.length > 0) || bodyText.length > 0);',
    '  }).catch(() => false);',
    '}',
    '',
    'async function waitForAppContent(page, timeout = 30000) {',
    '  await page.waitForFunction(() => {',
    '    const root = document.querySelector("#root");',
    '    const bodyText = document.body ? document.body.innerText.trim() : "";',
    '    return Boolean((root && root.children.length > 0) || bodyText.length > 0);',
    '  }, null, { timeout });',
    '}',
    '',
    'async function waitForRouteOrLogin(page, route, timeout = 60000, routeStableMs = 0) {',
    '  const startedAt = Date.now();',
    '  let routeSeenAt = null;',
    '  while (Date.now() - startedAt < timeout) {',
    '    const pathname = new URL(page.url()).pathname;',
    '    if (pathname.includes("/login")) return "login";',
    "    if (await page.getByRole('button', { name: /로그인|Login/i }).first().waitFor({ state: 'visible', timeout: 500 }).then(() => true).catch(() => false)) return 'login';",
    '    if (pathname === route && await hasAppContent(page)) {',
    '      routeSeenAt = routeSeenAt || Date.now();',
    '      if (Date.now() - routeSeenAt >= routeStableMs) return "route";',
    '    } else {',
    '      routeSeenAt = null;',
    '    }',
    '    await page.waitForTimeout(300);',
    '  }',
    '  return "unknown";',
    '}',
    '',
    'async function gotoRoute(page, route) {',
    "  await page.goto(route, { waitUntil: 'commit', timeout: 30000 });",
    "  await page.waitForLoadState('domcontentloaded', { timeout: 15000 }).catch(() => {});",
    '}',
    '',
    'async function performLogin(page) {',
    '  const username = process.env.E2E_USERNAME;',
    '  const password = process.env.E2E_PASSWORD;',
    "  const otp = process.env.E2E_OTP || '000000';",
    `  if (!username || !password) throw new Error(${JSON.stringify(missingCredentialsMessage)});`,
    '  const usernameInput = await firstVisible(page, [',
    "    'input[name=\"username\"]',",
    "    'input[id=\"username\"]',",
    "    'input[placeholder*=\"아이디\"]',",
    "    'input[placeholder*=\"사용자명\"]',",
    "    'input[type=\"text\"]',",
    "    'input:not([type])',",
    "    page.getByRole('textbox', { name: /아이디|사용자명|ID/i }),",
    '  ]);',
    "  if (!usernameInput) throw new Error('Login username input was not found.');",
    '  const passwordInput = await firstVisible(page, [',
    "    'input[name=\"password\"]',",
    "    'input[id=\"password\"]',",
    "    'input[placeholder*=\"비밀번호\"]',",
    "    'input[type=\"password\"]',",
    "    page.getByRole('textbox', { name: /비밀번호|Password/i }),",
    '  ]);',
    "  if (!passwordInput) throw new Error('Login password input was not found.');",
    '  await visibleFill(usernameInput, username);',
    '  await visibleFill(passwordInput, password);',
    '  const loginButton = await firstVisible(page, [',
    "    'button[type=\"submit\"]',",
    "    'input[type=\"submit\"]',",
    "    page.getByRole('button', { name: /로그인|Login/i }),",
    '  ]);',
    "  if (!loginButton) throw new Error('Login submit button was not found.');",
    '  await Promise.all([',
    "    page.waitForLoadState('domcontentloaded', { timeout: 30000 }).catch(() => {}),",
    '    loginButton.click(),',
    '  ]);',
    '  const otpInput = await page.locator(\'input[name="otp-0"], input[autocomplete="one-time-code"]\').first().waitFor({ state: "visible", timeout: 5000 }).then(() => page.locator(\'input[name="otp-0"], input[autocomplete="one-time-code"]\').first()).catch(() => null);',
    '  if (otpInput) {',
    '    await visibleFill(otpInput, otp);',
    '    const otpSubmit = await firstVisible(page, [\'button[type="submit"]\', page.getByRole("button", { name: /확인|인증|로그인|Submit/i })], 2000);',
    '    if (otpSubmit) await otpSubmit.click();',
    '  }',
    "  await page.waitForURL((url) => !url.pathname.includes('/login'), { timeout: 30000 }).catch(() => {});",
    '  if (await isLoginVisible(page, 1000)) {',
    "    const body = await page.locator('body').textContent().catch(() => '');",
    "    throw new Error(`Login did not complete. url=${page.url()} body=${String(body || '').slice(0, 300)}`);",
    '  }',
    '}',
    '',
    'async function checkStoredAuth(browser, route) {',
    '  if (!fs.existsSync(AUTH_STATE_FILE)) return false;',
    '  const context = await browser.newContext({ storageState: AUTH_STATE_FILE });',
    '  await installSavedSessionStorage(context);',
    '  const page = await context.newPage();',
    '  try {',
    '    await gotoRoute(page, route);',
    '    await page.waitForTimeout(1000);',
    '    const state = await waitForRouteOrLogin(page, route, 20000, 5000);',
    '    return state === "route" && !(await isLoginVisible(page, 1000));',
    '  } finally {',
    '    await context.close();',
    '  }',
    '}',
    '',
    'async function createAuthState(browser, route, testInfo) {',
    '  await fs.promises.mkdir(path.dirname(AUTH_STATE_FILE), { recursive: true });',
    '  const context = await browser.newContext();',
    '  const page = await context.newPage();',
    '  try {',
    '    await gotoRoute(page, route);',
    '    await page.waitForTimeout(1000);',
    '    const state = await waitForRouteOrLogin(page, route, 60000, 12000);',
    '    if (state === "login") {',
    '      await performLogin(page);',
    '      await gotoRoute(page, route);',
    '    }',
    '    if (await isLoginVisible(page, 1000)) {',
    "      throw new Error(`Still on login page after creating auth state. url=${page.url()}`);",
    '    }',
    '    await waitForAppContent(page, 30000);',
    '    await context.storageState({ path: AUTH_STATE_FILE });',
    '    await saveSessionStorage(page);',
    '  } catch (error) {',
    '    await attachFailureScreenshot(page, testInfo, "auth failure screenshot");',
    '    throw error;',
    '  } finally {',
    '    await context.close();',
    '  }',
    '}',
    '',
    'async function ensureAuthenticatedState(browser, route = TARGET_ROUTE, testInfo) {',
    '  if (process.env.COMMON_TESTER_AUTH_RESET !== "true" && await checkStoredAuth(browser, route)) return;',
    '  await createAuthState(browser, route, testInfo);',
    '}',
    '',
    'async function gotoAuthenticated(page, route) {',
    '  await gotoRoute(page, route);',
    "  await page.waitForLoadState('networkidle', { timeout: 15000 }).catch(() => {});",
    '  let state = await waitForRouteOrLogin(page, route, 30000, 4000);',
    '  if (state === "login" || await isLoginVisible(page, 1000)) {',
    '    await performLogin(page);',
    '    await page.context().storageState({ path: AUTH_STATE_FILE });',
    '    await saveSessionStorage(page);',
    '    await gotoRoute(page, route);',
    "    await page.waitForLoadState('networkidle', { timeout: 15000 }).catch(() => {});",
    '  }',
    '  state = await waitForRouteOrLogin(page, route, 30000, 2000);',
    '  if (state === "login" || await isLoginVisible(page, 1000)) {',
    "    throw new Error(`Authentication state is missing or expired before test steps. storageState=${AUTH_STATE_FILE} url=${page.url()}`);",
    '  }',
    '  const current = new URL(page.url());',
    '  if (current.pathname !== route) {',
    '    await gotoRoute(page, route);',
    '  }',
    '  await waitForAppContent(page, 30000);',
    '}',
    '',
  ].join('\n');
}

function renderAuthTestSetup(authRequired = true) {
  if (!authRequired) return '';
  return [
    'test.use({ storageState: AUTH_STATE_FILE });',
    '',
    'test.beforeAll(async ({ browser }, testInfo) => {',
    '  await ensureAuthenticatedState(browser, TARGET_ROUTE, testInfo);',
    '});',
    '',
  ].join('\n');
}

module.exports = { renderAuthHelper, renderAuthStatePath, renderAuthTestSetup };
